// Function to categorize project by type
export const categorizeProject = (projectName) => {
  const lowercaseName = projectName.toLowerCase();
  
  for (const [category, config] of Object.entries(projectTypes)) {
    if (config.keywords.some(keyword => lowercaseName.includes(keyword))) {
      return category;
    }
  }
  
  return 'Infrastructure & Development'; // default category
};

// Statistics
export const getProjectStats = () => {
  const totalProjects = projects.length;
  const completedProjects = projects.filter(p => p.status === 'Completed').length;
  const ongoingProjects = projects.filter(p => p.status === 'Ongoing').length;
  const provinces = [...new Set(projects.map(p => p.province))].length;
  const districts = [...new Set(projects.map(p => p.district))].length;
  
  return {
    totalProjects,
    completedProjects,
    ongoingProjects,
    provinces,
    districts
  };
};
