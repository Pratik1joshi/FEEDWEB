export const getTeamByDepartment = (department) => {
  if (department === "All") return teamMembers;
  return teamMembers.filter(member => member.department === department);
};

export const getTeamMemberById = (id) => {
  return teamMembers.find(member => member.id === parseInt(id));
};

export const getLeadershipTeam = () => {
  return teamMembers.filter(member => member.department === "Leadership");
};
