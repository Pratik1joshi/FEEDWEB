// Helper functions for filtering and categorization
export const getProjectsByCategory = (category) => {
  if (category === 'All') return detailedProjects;
  return detailedProjects.filter(project => project.category === category);
};

export const getProjectsByStatus = (status) => {
  return detailedProjects.filter(project => project.status === status);
};

export const getFeaturedProjects = () => {
  return detailedProjects; // All detailed projects can be featured
};

export const getProjectBySlug = (slug) => {
  return detailedProjects.find(project => project.slug === slug);
};

export const searchProjects = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return detailedProjects.filter(project => 
    project.title.toLowerCase().includes(lowercaseQuery) ||
    project.description.toLowerCase().includes(lowercaseQuery) ||
    project.location.toLowerCase().includes(lowercaseQuery) ||
    project.category.toLowerCase().includes(lowercaseQuery)
  );
};

